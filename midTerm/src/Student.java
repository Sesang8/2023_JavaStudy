
public class Student {
	private String name;
	private int num;
	private double grade;
	
	public Student(String name, int num, double grade) {
		this.name = name;
		this.num = num;
		this.grade = grade;
	}
	
	public String getName() {
		return name;
	}
	
	public int getNum() {
		return num;
	}
	
	public double getGrade() {
		return grade;
	}

}
